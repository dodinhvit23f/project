    public void Reload(List<Products> list){
       long start = System.currentTimeMillis();
       DefaultTableModel dm = new DefaultTableModel()
        {
            //  Returning the Class of each column will allow different
            //  renderers to be used based on Class
               @Override
            public Class getColumnClass(int column)
            {
                return getValueAt(0, column).getClass();
            }
            
                        
        };
        this.jTable1.clearSelection();
        dm.addColumn("Number");
        dm.addColumn("Image");
        dm.addColumn("Name");
        dm.addColumn("Kind");
        dm.addColumn("Price In");
        dm.addColumn("Price Out");
        dm.addColumn("Import Date");
        dm.addColumn("Exp Date");
        dm.addColumn("Quantity");
        dm.addColumn("Action");
        ImageIcon icon  = null;
        for(Products ele : list){
            if(ele.getUrl() != null || !ele.getUrl().equals("")){
                icon = new ImageIcon(ele.getUrl());
                icon.setImage(icon.getImage().getScaledInstance(100, 100,Image.SCALE_DEFAULT));
                icon.setDescription("Image");
                try {	
                   
                    dm.addRow(new Object[]{
                        ele.getId()
                        ,icon,ele.getName()
                        ,ele.getKind()
                        ,ele.getPriceIn()
                        ,ele.getPriceOut()
                        ,Utility.dateToString(ele.getImportDate())
                        ,Utility.dateToString(ele.getExpDate())
                        ,ele.getQuantities()
                        ,"Delete"
                    });
                } catch (Exception ex) {
                    Logger.getLogger(HomeProduct.class.getName()).log(Level.SEVERE, null, ex);
                }
            } 
        }
        
        this.jTable1.setRowHeight(100);
      //  this.jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        this.jTable1.setModel(dm);
        new ButtonColumn (this.jTable1,9,ss ->{ 
        	int row =  this.jTable1.getSelectedRow();
        	Object value = this.jTable1.getValueAt(row, 0);
        	if(JOptionPane.showConfirmDialog(null, " Do you want to delete this") == JOptionPane.YES_OPTION) {
        		dao.delete(String.valueOf(value));
                        this.Reload(dao.selectAll());
        	}
        });
        //totalPage = Utility.convertInt(dao.getTotalPage(limmit).toString()); 
        System.out.println(" load data  : " + (System.currentTimeMillis() - start) );
        totalPage = (int)Math.ceil(Utility.convertDouble(dao.getTotalPage(limmit).toString()));
       // this.jTable1.setEnabled(false);
    }

